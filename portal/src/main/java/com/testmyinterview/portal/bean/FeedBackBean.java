package com.testmyinterview.portal.bean;

public class FeedBackBean {
	
	int interViewId;
	int evalRating;
	int evalId;
	String evalNote;
	int userType;
	int feedBackStatusId;
	
	
	
	public int getInterViewId() {
		return interViewId;
	}
	public void setInterViewId(int interViewId) {
		this.interViewId = interViewId;
	}
	public int getEvalRating() {
		return evalRating;
	}
	public void setEvalRating(int evalRating) {
		this.evalRating = evalRating;
	}
	public int getEvalId() {
		return evalId;
	}
	public void setEvalId(int evalId) {
		this.evalId = evalId;
	}
	public String getEvalNote() {
		return evalNote;
	}
	public void setEvalNote(String evalNote) {
		this.evalNote = evalNote;
	}
	public int getUserType() {
		return userType;
	}
	public void setUserType(int userType) {
		this.userType = userType;
	}
	public int getFeedBackStatusId() {
		return feedBackStatusId;
	}
	public void setFeedBackStatusId(int feedBackStatusId) {
		this.feedBackStatusId = feedBackStatusId;
	}
	
	
}